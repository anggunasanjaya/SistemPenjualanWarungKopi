    public void showTableBarang() throws SQLException {
        DefaultTableModel dtmbarang = new DefaultTableModel(new String[]{"ID","NAMA BARANG", "STOCK BARANG", "HARGA BARANG"}, 0);

        for (Barang b : control.getDataBarang()) {
            dtmbarang.addRow(new String[]{b.getId_barang().toString(),b.getNama_barang().toString(), b.getStock_barang().toString(), b.getHarga_barang().toString()});
        }
        tblBarang.setModel(dtmbarang);
    }

    public void showcomboBoxJenisBarang() throws SQLException {
        DefaultComboBoxModel dcbmJenisBarang = new DefaultComboBoxModel();
        dcbmJenisBarang.addElement("Pilih Jenis barang ");
        for (Jenis_Barang jb : control.getDataJenisBarang()) {
            dcbmJenisBarang.addElement(jb.getIdjenisbarang()+" - "+jb.getNama_jenis_barang());
        }
        this.cbInsertJenisBarang.removeAllItems();
        this.cbInsertJenisBarang.setModel(dcbmJenisBarang);
    }

