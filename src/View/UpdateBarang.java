    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed

        try {
            Barang b = new Barang();
            String item = cbInsertJenisBarang.getSelectedItem().toString();
            String id = item.substring(0, item.indexOf(" -"));
            Jenis_Barang jb = new Jenis_Barang();
            jb.setIdjenisbarang(id);
            b.setJenis_barang(jb);
            b.setNama_barang(tfInsertNamaBarang.getText());
            b.setHarga_barang(Integer.valueOf(tfInsertHargaBarang.getText()));
            b.setStock_barang(Integer.parseInt(tfInsertStockBarang.getText()));

            control.insertBarang(b);
            showTableBarang();

        } catch (SQLException ex) {
            Logger.getLogger(UpdateBarang.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnInsertActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed

        try {
            Barang b = control.getDataBarang().get(tblBarang.getSelectedRow());
            control.deleteBarang(b);
            showTableBarang();
        } catch (SQLException ex) {
            Logger.getLogger(UpdateJenis_Barang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        try {
            Barang b = control.getDataBarang().get(tblBarang.getSelectedRow());
            int harga = Integer.parseInt(btnUpdate.getText());
            int Stock = Integer.parseInt(btnUpdate.getText());

            control.updateBarang(b, harga, Stock);
            showTableBarang();
        } catch (SQLException ex) {
            Logger.getLogger(UpdateBarang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

