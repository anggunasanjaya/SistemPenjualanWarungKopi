    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
try {
            int col = 0;
            int row = tblBarang.getSelectedRow();
            System.out.println("ROW = " + row);
            if (row < 0) {
                JOptionPane.showMessageDialog(null, "Barang belum dipilih, Pilih barang terlebih dahulu !");
            } else {
                String id = tblBarang.getModel().getValueAt(row, col).toString();
                System.out.println("ID = " + id);
                Barang brg = control.getDataBarang(Integer.parseInt(id));
                
                tfIdBarang.setText(String.valueOf(brg.getId_barang()));
                tfUpdateStockBarang.setText(brg.getStock_barang().toString());
                tfUpdateHargaBarang.setText(brg.getHarga_barang().toString());
            }
        } catch (SQLException ex) {
            Logger.getLogger(UpdateBarang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEditActionPerformed

